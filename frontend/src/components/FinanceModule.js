function allocatePaymentAcrossInvoices(invoices, totalAmount) {
  const sorted = [...invoices].sort((a, b) => {
    const dateA = new Date(a?.issueDate || a?.dueDate || 0).getTime();
    const dateB = new Date(b?.issueDate || b?.dueDate || 0).getTime();
    if (dateA !== dateB) return dateA - dateB;
    return String(a?.id || "").localeCompare(String(b?.id || ""));
  });
  let remaining = totalAmount;
  const allocations = [];
  for (const inv of sorted) {
    if (remaining <= 0.009) break;
    const due = invoiceBalanceDue(inv);
    const alloc = Math.min(remaining, due);
    if (alloc > 0.009) {
      allocations.push({ invoice: inv, amount: alloc });
      remaining -= alloc;
    }
  }
  return { allocations, unallocated: remaining };
}

function collectInvoiceEvidenceItems(inv) {
  const items = [];
  const history = Array.isArray(inv?.paymentProofHistory) ? inv.paymentProofHistory : [];
  for (const entry of history) {
    const url = String(entry?.url || "").trim();
    const claimed = Number(entry?.claimedAmount);
    const approved = Number(entry?.approvedAmount);
    const hasAmount = (Number.isFinite(claimed) && claimed > 0) || (Number.isFinite(approved) && approved > 0);
    const isRefund = String(entry?.outcome || "").trim() === "refund";
    if (!url && !hasAmount && !isRefund) continue;
    items.push({
      url,
      name: String(entry?.name || "Payment evidence").trim(),
      uploadedAt: String(entry?.uploadedAt || "").trim(),
      outcome: String(entry?.outcome || (entry?.rejectionReason ? "rejected" : "superseded")).trim(),
      rejectionReason: String(entry?.rejectionReason || "").trim(),
      claimedAmount: entry?.claimedAmount,
      approvedAmount: entry?.approvedAmount,
      balanceDueAtUpload: entry?.balanceDueAtUpload,
      paidBeforeUpload: entry?.paidBeforeUpload,
      isCurrent: false
    });
  }
  const currentUrl = String(inv?.paymentProofUrl || "").trim();
  const status = String(inv?.status || "").trim();
  if (currentUrl) {
    let outcome = "pending";
    if (status === "Paid" && isInvoiceFullyPaid(inv)) outcome = "approved";
    else if (status === "Verifying") outcome = "pending";
    else if (hasRejectedPaymentEvidence(inv)) outcome = "rejected";
    items.push({
      url: currentUrl,
      name: String(inv?.paymentProofName || "Payment evidence").trim(),
      uploadedAt: String(inv?.paymentProofUploadedAt || inv?.updatedAt || inv?.issueDate || "").trim(),
      outcome,
      rejectionReason: String(inv?.paymentRejectionReason || "").trim(),
      claimedAmount: inv?.paymentProofClaimedAmount,
      balanceDueAtUpload: inv?.paymentProofBalanceDue,
      paidBeforeUpload: inv?.paymentProofPaidBefore,
      isCurrent: true
    });
  } else if (status === "Verifying") {
    const claimed = Number(inv?.paymentProofClaimedAmount);
    if (Number.isFinite(claimed) && claimed > 0) {
      items.push({
        url: "",
        name: String(inv?.paymentMethod || "").trim() === "Cash" ? "Cash payment" : "Payment evidence",
        uploadedAt: String(inv?.paymentProofUploadedAt || inv?.updatedAt || inv?.issueDate || "").trim(),
        outcome: "pending",
        rejectionReason: String(inv?.paymentRejectionReason || "").trim(),
        claimedAmount: inv?.paymentProofClaimedAmount,
        balanceDueAtUpload: inv?.paymentProofBalanceDue,
        paidBeforeUpload: inv?.paymentProofPaidBefore,
        isCurrent: true
      });
    }
  }
  return items;
}

function buildPaymentTableRows(invoices) {
  const rows = [];
  for (const inv of invoices) {
    const evidences = collectInvoiceEvidenceItems(inv);
    if (evidences.length === 0 && invoiceApprovedPaid(inv) > 0) {
      evidences.push({
        url: String(inv?.paymentProofUrl || "").trim(),
        name: String(inv?.paymentProofName || "Payment").trim(),
        uploadedAt: String(inv?.paidAmountRecordedAt || inv?.updatedAt || inv?.issueDate || "").trim(),
        outcome: "approved",
        rejectionReason: "",
        approvedAmount: invoiceApprovedPaid(inv),
        claimedAmount: inv?.paymentProofClaimedAmount,
        isCurrent: false
      });
    }
    evidences.forEach((evidence, idx) => {
      const outcome = String(evidence?.outcome || "").trim();
      if (outcome === "superseded") return;
      if (outcome !== "approved" && outcome !== "pending" && outcome !== "rejected" && outcome !== "refund") return;
      rows.push({
        invoice: inv,
        evidence,
        rowKey: `inv-${inv.id}-ev-${idx}-${evidence.uploadedAt || idx}`
      });
    });
  }
  return rows;
}

function isImageProof(url, name) {
  const hint = `${String(name || "")} ${String(url || "")}`.toLowerCase();
  if (hint.includes("data:image/")) return true;
  return /\.(png|jpe?g|gif|webp|bmp)$/i.test(hint.split("?")[0]);
}

function isPdfProof(url, name) {
  const hint = `${String(name || "")} ${String(url || "")}`.toLowerCase();
  return hint.includes("application/pdf") || /\.pdf$/i.test(hint.split("?")[0]);
}

